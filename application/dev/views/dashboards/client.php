	<p class="lead">As part of <em>Closing the Gap</em> initiative in Tackling Indigenous Chronic Disease, Far North Regional Tackling Smoking and Healthy Lifestyle Team has created the D Warrior Character utilising it as a means to deliver its messages to the Aboriginal and Torres Strait Islander communities in the region and more broadly, making an impact to wider communities right across the nation.</p>
	<p>In bringing this character to life, the purpose is for the D Warrior to act as a message stick that delivers positive messages to community's and it's people to stay healthy, deadly and strong.</p>
	<p>The aims for the creation of such a superhero character and materialising this character in all communities is to act as the educator at the forefront of harm associated with smoking tobacco and to relay key messages that highlight the 3 D's of:</p>
	
	<ul class="lead">
	  <li>Durries Do Destroy,</li>
	  <li>Danger from Tobacco Smoking,</li>
	  <li>Diseases from Tobacco Smoking</li>
	</ul>
	
	<p>We hope that the Superhero character can also deliver its messages <strong>(Tackling Tobacco Smoking Head On)</strong> to the wider community as tobacco smoking are not just an Aboriginal and Torres Strait Islander people's problem but is something that affects everyone.</p>
